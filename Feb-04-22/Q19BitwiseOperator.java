package basic75programs;

public class Q19BitwiseOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        int c = 20;
        System.out.println(a & c);
        System.out.println(b ^ c);
        System.out.println(a | b);
        System.out.println(~b);
    }
}
